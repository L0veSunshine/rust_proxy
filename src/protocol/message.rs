// Client Hello
// +------+------+------+------------------+------+---------------------------+---------+---------------------------+
// | Magic| Ver  | Type |    User Token    | Cmd  |      Target Address       | Pad Len |          Padding          |
// | 0xCC | 0x01 | 0x01 |  (UUID 128-bit)  | 0x01 |     (ATYP + Addr + Port)  |  (1B)   |       (0-255 Bytes)       |
// +------+------+------+------------------+------+---------------------------+---------+---------------------------+

// Server Response
// +--------+
// | Status |
// |  0x00  |
// +--------+

// Target Address Ipv4
// +------+-----------------------+-----------------------+
// | ATYP |       IPv4 Addr       |         Port          |
// | 0x01 |       (4 Bytes)       |       (2 Bytes)       |
// +------+-----------------------+-----------------------+
//    |              |                        |
//    |              |                        +---> Big Endian (e.g. 443 = 0x01 0xBB)
//    |              +---> e.g. 192.168.1.1 = C0 A8 01 01
//    +---> Type 1

// Target Address Domain
// +------+------+-------------------------------+-----------------------+
// | ATYP | Len  |            Domain             |         Port          |
// | 0x03 | (1B) |          (Len Bytes)          |       (2 Bytes)       |
// +------+------+-------------------------------+-----------------------+
//    |      |                  |                            |
//    |      |                  |                            +---> 端口
//    |      |                  +---> "google.com" (ASCII/UTF8)
//    |      +---> 域名长度 (e.g. 10)
//    +---> Type 3

// Target Address Ipv6
// +------+---------------------------------------------------------------+-----------------------+
// | ATYP |                           IPv6 Addr                           |         Port          |
// | 0x04 |                          (16 Bytes)                           |       (2 Bytes)       |
// +------+---------------------------------------------------------------+-----------------------+

// Data Transfer
// +-----------+---------------------------+-------------------------------+
// | Body Len  |      Target Address       |            Payload            |
// | (2 Bytes) |        (Variable)         |           (N Bytes)           |
// +-----------+---------------------------+-------------------------------+
//      ^                   ^
//      |                   |
//      |                   +---> Target Address
//      +---> Big Endian, 表示后续数据的总长度 (Addr + Payload)
